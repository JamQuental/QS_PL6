package org.example;

public class Exercicio3_Nota {
    int numAluno;
    int nota;

    public Exercicio3_Nota(int numAluno, int nota) {
        this.numAluno = numAluno;
        this.nota = nota;
    }

    public int getNumAluno() {
        return numAluno;
    }

    public int getNota() {
        return nota;
    }
}
